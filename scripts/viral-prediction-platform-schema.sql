-- Trendzo Viral Prediction Platform Database Schema Extensions
-- This script extends the existing Supabase database for the two-tier viral prediction platform

-- Enable UUID extension if not already enabled
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- =============================================================================
-- LIMITED USER MANAGEMENT TABLES
-- =============================================================================

-- Limited users with controlled access
CREATE TABLE IF NOT EXISTS limited_users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email TEXT UNIQUE NOT NULL,
    tiktok_username TEXT,
    access_granted_at TIMESTAMPTZ DEFAULT NOW(),
    access_expires_at TIMESTAMPTZ,
    daily_analysis_limit INTEGER DEFAULT 3,
    analyses_used_today INTEGER DEFAULT 0,
    last_analysis_reset DATE DEFAULT CURRENT_DATE,
    features_enabled JSONB DEFAULT '{"video_analysis": true, "template_access": "top5", "optimization_suggestions": "basic"}',
    referral_source TEXT, -- which marketing video brought them
    status TEXT DEFAULT 'active', -- active, suspended, expired
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Usage tracking for limited users
CREATE TABLE IF NOT EXISTS user_analytics (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES limited_users(id) ON DELETE CASCADE,
    action_type TEXT NOT NULL, -- 'video_analyzed', 'template_viewed', 'optimization_applied', etc
    details JSONB,
    video_id TEXT, -- if action relates to a specific video
    template_id TEXT, -- if action relates to a specific template
    session_id TEXT, -- track user sessions
    ip_address INET,
    user_agent TEXT,
    timestamp TIMESTAMPTZ DEFAULT NOW()
);

-- Success stories and testimonials
CREATE TABLE IF NOT EXISTS success_stories (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES limited_users(id) ON DELETE SET NULL,
    before_metrics JSONB, -- views, likes, etc before using platform
    after_metrics JSONB, -- performance after using platform
    testimonial_text TEXT,
    video_url TEXT,
    improvement_percentage DECIMAL,
    verified BOOLEAN DEFAULT FALSE,
    featured BOOLEAN DEFAULT FALSE, -- highlight best testimonials
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- =============================================================================
-- INCEPTION MARKETING TRACKING
-- =============================================================================

-- Marketing campaigns generated by Inception Studio
CREATE TABLE IF NOT EXISTS marketing_campaigns (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    campaign_name TEXT NOT NULL,
    video_title TEXT,
    video_description TEXT,
    platform TEXT DEFAULT 'tiktok',
    viral_prediction DECIMAL, -- predicted viral probability
    actual_views INTEGER DEFAULT 0,
    actual_likes INTEGER DEFAULT 0,
    comments_received INTEGER DEFAULT 0,
    shares_received INTEGER DEFAULT 0,
    conversions INTEGER DEFAULT 0, -- users who signed up from this video
    conversion_rate DECIMAL,
    roi_estimate DECIMAL,
    status TEXT DEFAULT 'active', -- active, paused, completed
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Track which marketing videos led to user signups
CREATE TABLE IF NOT EXISTS marketing_conversions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    campaign_id UUID REFERENCES marketing_campaigns(id) ON DELETE SET NULL,
    user_id UUID REFERENCES limited_users(id) ON DELETE CASCADE,
    conversion_source TEXT, -- 'comment', 'bio_link', 'direct'
    referral_timestamp TIMESTAMPTZ DEFAULT NOW(),
    first_action_timestamp TIMESTAMPTZ, -- when they first used the platform
    lifetime_value DECIMAL DEFAULT 0 -- track user value over time
);

-- =============================================================================
-- SUPERADMIN SYSTEM CONTROL
-- =============================================================================

-- Module status tracking for Mission Control Dashboard
CREATE TABLE IF NOT EXISTS module_status (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    module_name TEXT NOT NULL,
    status TEXT NOT NULL, -- 'running', 'idle', 'error', 'maintenance'
    throughput_per_hour INTEGER DEFAULT 0,
    last_run TIMESTAMPTZ,
    last_success TIMESTAMPTZ,
    error_count INTEGER DEFAULT 0,
    performance_metrics JSONB, -- response times, success rates, etc
    config JSONB, -- module configuration
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(module_name)
);

-- Real-time pipeline flow tracking
CREATE TABLE IF NOT EXISTS pipeline_flows (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    flow_id TEXT NOT NULL, -- unique identifier for this data flow
    source_module TEXT,
    target_module TEXT,
    data_type TEXT, -- 'video_batch', 'gene_vectors', 'templates', etc
    records_processed INTEGER,
    processing_time_ms INTEGER,
    status TEXT, -- 'processing', 'completed', 'failed'
    error_message TEXT,
    started_at TIMESTAMPTZ DEFAULT NOW(),
    completed_at TIMESTAMPTZ
);

-- =============================================================================
-- PREDICTION VALIDATION & ACCURACY TRACKING
-- =============================================================================

-- Track prediction accuracy over time
CREATE TABLE IF NOT EXISTS prediction_validation (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    video_id TEXT NOT NULL,
    predicted_probability DECIMAL NOT NULL,
    prediction_engine TEXT NOT NULL, -- 'DNA_Detective', 'QuantumSwarmNexus', etc
    prediction_timestamp TIMESTAMPTZ NOT NULL,
    actual_views_24h INTEGER,
    actual_likes_24h INTEGER,
    actual_shares_24h INTEGER,
    actual_comments_24h INTEGER,
    actual_viral_score DECIMAL, -- calculated viral score after 24h/48h
    validation_timestamp TIMESTAMPTZ,
    accuracy_score DECIMAL, -- how close the prediction was
    niche TEXT,
    platform TEXT DEFAULT 'tiktok'
);

-- System-wide accuracy metrics
CREATE TABLE IF NOT EXISTS accuracy_metrics (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    engine_name TEXT NOT NULL,
    time_period TEXT NOT NULL, -- 'daily', 'weekly', 'monthly'
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    total_predictions INTEGER,
    accurate_predictions INTEGER, -- within threshold
    accuracy_percentage DECIMAL,
    avg_confidence_score DECIMAL,
    niche_breakdown JSONB, -- accuracy by niche
    created_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(engine_name, time_period, start_date)
);

-- =============================================================================
-- TEMPLATE INTELLIGENCE & STATUS
-- =============================================================================

-- Extended template status (HOT/COOLING/NEW/DYING)
CREATE TABLE IF NOT EXISTS template_intelligence (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    template_id TEXT NOT NULL,
    status TEXT NOT NULL, -- 'HOT', 'COOLING', 'NEW', 'STABLE', 'DYING'
    temperature_score DECIMAL, -- 0-100 viral temperature
    trend_velocity DECIMAL, -- rate of change
    prediction_confidence DECIMAL,
    viral_count_24h INTEGER DEFAULT 0,
    viral_count_7d INTEGER DEFAULT 0,
    usage_by_limited_users INTEGER DEFAULT 0, -- how many limited users tried this
    success_rate_limited_users DECIMAL, -- success rate among limited users
    last_updated TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(template_id)
);

-- =============================================================================
-- RATE LIMITING & SECURITY
-- =============================================================================

-- Rate limiting tracking
CREATE TABLE IF NOT EXISTS rate_limits (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES limited_users(id) ON DELETE CASCADE,
    ip_address INET,
    endpoint TEXT,
    requests_count INTEGER DEFAULT 1,
    window_start TIMESTAMPTZ DEFAULT NOW(),
    window_end TIMESTAMPTZ,
    blocked BOOLEAN DEFAULT FALSE
);

-- Security events and monitoring
CREATE TABLE IF NOT EXISTS security_events (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES limited_users(id) ON DELETE SET NULL,
    event_type TEXT NOT NULL, -- 'login_attempt', 'rate_limit_exceeded', 'suspicious_activity'
    ip_address INET,
    user_agent TEXT,
    details JSONB,
    severity TEXT DEFAULT 'info', -- 'info', 'warning', 'critical'
    resolved BOOLEAN DEFAULT FALSE,
    timestamp TIMESTAMPTZ DEFAULT NOW()
);

-- =============================================================================
-- PERFORMANCE MONITORING
-- =============================================================================

-- API endpoint performance tracking
CREATE TABLE IF NOT EXISTS api_performance (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    endpoint TEXT NOT NULL,
    method TEXT NOT NULL,
    response_time_ms INTEGER,
    status_code INTEGER,
    user_type TEXT, -- 'superadmin', 'limited_user', 'anonymous'
    timestamp TIMESTAMPTZ DEFAULT NOW()
);

