-- =============================================================================
-- TRIGGERS FOR AUTOMATIC UPDATES
-- =============================================================================

-- Update timestamp trigger function
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Apply update triggers
CREATE TRIGGER update_limited_users_updated_at BEFORE UPDATE ON limited_users FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
CREATE TRIGGER update_success_stories_updated_at BEFORE UPDATE ON success_stories FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
CREATE TRIGGER update_marketing_campaigns_updated_at BEFORE UPDATE ON marketing_campaigns FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- Reset daily analysis counter trigger
CREATE OR REPLACE FUNCTION reset_daily_analysis_counter()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.last_analysis_reset < CURRENT_DATE THEN
        NEW.analyses_used_today = 0;
        NEW.last_analysis_reset = CURRENT_DATE;
    END IF;
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER reset_analysis_counter BEFORE UPDATE ON limited_users FOR EACH ROW EXECUTE FUNCTION reset_daily_analysis_counter();

