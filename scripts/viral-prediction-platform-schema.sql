-- Real-time module health view
CREATE OR REPLACE VIEW module_health_dashboard AS
SELECT 
    module_name,
    status,
    throughput_per_hour,
    last_run,
    last_success,
    error_count,
    CASE 
        WHEN status = 'running' AND last_success > NOW() - INTERVAL '1 hour' THEN 'healthy'
        WHEN status = 'idle' THEN 'idle'
        WHEN error_count > 5 THEN 'unhealthy'
        ELSE 'warning'
    END as health_status,
    performance_metrics
FROM module_status
ORDER BY 
    CASE status 
        WHEN 'error' THEN 1 
        WHEN 'running' THEN 2 
        WHEN 'idle' THEN 3 
        ELSE 4 
    END;

-- Template intelligence summary
CREATE OR REPLACE VIEW template_status_summary AS
SELECT 
    status,
    COUNT(*) as template_count,
    AVG(temperature_score) as avg_temperature,
    AVG(trend_velocity) as avg_velocity,
    SUM(viral_count_24h) as total_viral_24h
FROM template_intelligence
GROUP BY status
ORDER BY 
    CASE status 
        WHEN 'HOT' THEN 1 
        WHEN 'NEW' THEN 2 
        WHEN 'STABLE' THEN 3 
        WHEN 'COOLING' THEN 4 
        WHEN 'DYING' THEN 5 
    END;

-- Limited user activity summary
CREATE OR REPLACE VIEW limited_user_activity AS
SELECT 
    DATE(timestamp) as activity_date,
    COUNT(DISTINCT user_id) as active_users,
    COUNT(*) as total_actions,
    COUNT(*) FILTER (WHERE action_type = 'video_analyzed') as video_analyses,
    COUNT(*) FILTER (WHERE action_type = 'template_viewed') as template_views
FROM user_analytics
WHERE timestamp > NOW() - INTERVAL '30 days'
GROUP BY DATE(timestamp)
ORDER BY activity_date DESC;

-- Marketing campaign performance
CREATE OR REPLACE VIEW marketing_performance AS
SELECT 
    mc.campaign_name,
    mc.viral_prediction,
    mc.actual_views,
    mc.conversions,
    mc.conversion_rate,
    COUNT(mcv.user_id) as total_signups,
    AVG(mcv.lifetime_value) as avg_lifetime_value
FROM marketing_campaigns mc
LEFT JOIN marketing_conversions mcv ON mc.id = mcv.campaign_id
WHERE mc.status = 'active'
GROUP BY mc.id, mc.campaign_name, mc.viral_prediction, mc.actual_views, mc.conversions, mc.conversion_rate
ORDER BY mc.conversion_rate DESC NULLS LAST;

