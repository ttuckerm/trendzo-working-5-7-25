-- Prediction Validation Table
-- Stores predictions for accuracy tracking and validation

CREATE TABLE IF NOT EXISTS prediction_validation (
    id SERIAL PRIMARY KEY,
    prediction_id VARCHAR(100) UNIQUE NOT NULL,
    video_id VARCHAR(200) NOT NULL,
    predicted_viral_score DECIMAL(5,2) NOT NULL,
    predicted_views INTEGER,
    predicted_viral_probability DECIMAL(4,3),
    actual_viral_score DECIMAL(5,2),
    actual_views INTEGER,
    actual_viral_probability DECIMAL(4,3),
    accuracy_percentage DECIMAL(5,2),
    validation_status VARCHAR(20) DEFAULT 'pending' CHECK (validation_status IN ('pending', 'validated', 'failed')),
    validation_timestamp TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    platform VARCHAR(20) DEFAULT 'tiktok',
    
    -- CRITICAL: Adding the missing metadata column that was causing the error
    metadata JSONB,
    
    -- Performance tracking
    processing_time_ms INTEGER,
    confidence_level DECIMAL(4,3),
    
    -- Indexes for performance
    CONSTRAINT unique_prediction_id UNIQUE (prediction_id)
);

-- Index for finding pending validations
CREATE INDEX IF NOT EXISTS idx_prediction_validation_status 
ON prediction_validation(validation_status, created_at DESC);

-- Index for accuracy queries
CREATE INDEX IF NOT EXISTS idx_prediction_validation_accuracy 
ON prediction_validation(validation_status, accuracy_percentage DESC);

-- Index for video ID lookups
CREATE INDEX IF NOT EXISTS idx_prediction_validation_video_id 
ON prediction_validation(video_id);

-- Index for platform filtering
CREATE INDEX IF NOT EXISTS idx_prediction_validation_platform 
ON prediction_validation(platform, validation_status);

-- Index for timestamp queries
CREATE INDEX IF NOT EXISTS idx_prediction_validation_timestamp 
ON prediction_validation(validation_timestamp DESC);

-- Index for metadata queries (GIN index for JSONB)
CREATE INDEX IF NOT EXISTS idx_prediction_validation_metadata 
ON prediction_validation USING GIN (metadata);

-- Comment explaining the table
COMMENT ON TABLE prediction_validation IS 'Stores viral predictions for accuracy tracking and validation with comprehensive metadata';

-- Column comments
COMMENT ON COLUMN prediction_validation.metadata IS 'JSONB field storing algorithm inputs, component scores, and diagnostic information';
COMMENT ON COLUMN prediction_validation.prediction_id IS 'Unique identifier for each prediction generated by the master algorithm';
COMMENT ON COLUMN prediction_validation.accuracy_percentage IS 'Calculated accuracy when actual results are available';
COMMENT ON COLUMN prediction_validation.validation_status IS 'Status of validation: pending, validated, or failed';