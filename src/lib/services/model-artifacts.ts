import { promises as fs } from 'fs';
import * as path from 'path';

const ROOT = path.join(process.cwd(), 'storage', 'models');

export async function saveModel(platform: string, version: string, payload: object) {
  const dir = path.join(ROOT, platform);
  await fs.mkdir(dir, { recursive: true });
  await fs.writeFile(path.join(dir, `${version}.json`), JSON.stringify(payload));
}

export async function loadModel(platform: string, version: string) {
  const buf = await fs.readFile(path.join(ROOT, platform, `${version}.json`), 'utf-8');
  return JSON.parse(buf);
}






















































































































































