import { buildTrend, computeDriftIndex } from '@/lib/learning/summary'

describe('learning.summary', () => {
  it('builds 30-day trend and drift index is within [0,1]', () => {
    process.env.MOCK = '1'
    const trend = buildTrend(30)
    expect(Array.isArray(trend)).toBe(true)
    expect(trend.length).toBeGreaterThanOrEqual(14)
    const d = computeDriftIndex()
    expect(d).toBeGreaterThanOrEqual(0)
    expect(d).toBeLessThanOrEqual(1)
  })
})








































































































































