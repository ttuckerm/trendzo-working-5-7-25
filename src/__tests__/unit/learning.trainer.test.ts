import { runTrainer } from '@/lib/learning/trainer'
import { getCurrentModel } from '@/lib/learning/store'

describe('learning.trainer', () => {
  it('improves brier/ece and bumps version', async () => {
    process.env.MOCK = '1'
    const cur = await getCurrentModel()
    const { candidate } = await runTrainer()
    expect(candidate.version).toBe(cur.version + 1)
    // In mock fixtures this should generally improve or at least not worsen a lot
    expect(candidate.metricsAtBuild.brier).toBeLessThanOrEqual((cur.metricsAtBuild?.brier ?? 1))
  })
})








































































































































