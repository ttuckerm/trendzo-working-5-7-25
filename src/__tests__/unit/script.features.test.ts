import { extractScriptFeatures } from '@/lib/script/features'

describe('script features', () => {
  it('detects question-first and numbers and CTA', () => {
    const f = extractScriptFeatures('What if you could get 3x more leads? Follow for more!')
    expect(f.questions).toBeGreaterThan(0)
    expect(f.numbers).toBeGreaterThan(0)
    expect(f.ctaDetected).toBe(true)
  })

  it('detects before/after and imperative verbs', () => {
    const f = extractScriptFeatures('Before: broke. After: profitable. Do this now.')
    expect(f.beforeAfterDetected).toBe(true)
    expect(f.imperativeVerbs.length).toBeGreaterThan(0)
  })
})








































































































































