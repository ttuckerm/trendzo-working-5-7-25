import { GET as SUMMARY } from '@/app/api/learning/summary/route'

describe('/api/learning/summary', () => {
  it('returns LearningSummary with trend and drift', async () => {
    process.env.MOCK = '1'
    const res = await SUMMARY()
    expect(res.status).toBe(200)
    const json = await res.json()
    expect(json.currentVersion).toBeGreaterThan(0)
    expect(Array.isArray(json.accuracyTrend)).toBe(true)
    expect(json.accuracyTrend.length).toBeGreaterThanOrEqual(14)
    expect(json.driftIndex).toBeGreaterThanOrEqual(0)
    expect(json.driftIndex).toBeLessThanOrEqual(1)
  })
})








































































































































