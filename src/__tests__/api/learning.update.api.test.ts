import { POST as UPDATE } from '@/app/api/learning/update/route'

describe('/api/learning/update', () => {
  it('returns candidate with metrics', async () => {
    process.env.MOCK = '1'
    const res = await UPDATE()
    expect(res.status).toBe(200)
    const json = await res.json()
    expect(json.candidate?.version).toBeGreaterThan(0)
    expect(json.candidate?.metricsAtBuild?.validated).toBeGreaterThanOrEqual(0)
  })
})








































































































































